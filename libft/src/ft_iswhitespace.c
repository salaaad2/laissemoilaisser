#include <libft.h>

t_bool	ft_iswhitespace(int c)
{
	if (c == '\t'
		|| c == '\v'
		|| c == '\f'
		|| c == '\r'
		|| c == ' ')
	{
		return (TRUE);
	}
	return (FALSE);
}
